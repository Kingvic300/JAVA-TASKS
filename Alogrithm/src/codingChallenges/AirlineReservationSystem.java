package codingChallenges;

import java.util.ArrayList;

public class AirlineReservationSystem {
    public static ArrayList<Integer> planeCapacity(ArrayList <Integer> planeCapacity) {
        ArrayList<Integer> number = new ArrayList<>();
        for (Integer integer : planeCapacity) {
            number.add(integer);
        }
        return number;
    }

    public static ArrayList<Integer> firstClass(ArrayList<Integer> planeCapacity) {
        ArrayList<Integer> number = new ArrayList<>();
        for (Integer integer : planeCapacity) {
            number.add(integer);
        }
        return number;
    }
}
